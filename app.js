// The four numeric operations functions

// Addition
function addition(val1, val2) {
  return val1 + val2;
}

// Subtraction
function subtraction(val1, val2) {
  return val1 - val2;
}

// Multiplication
function multiplication(val1, val2) {
  return val1 * val2;
}

// Division
function division(val1, val2) {
  return val1 / val2;
}